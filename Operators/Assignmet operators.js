let x = 5;
let y = 10;
  console.log(x += y); //output 15
 console.log(x -= y); //output 5
 console.log(x *= y); //output 50
 console.log(x /= y); //output 5
 console.log(x %= y); //output 5
console.log(x **= y); //output 5^10 = 9765625
console.log(x &= y); //output 0
console.log(x |= y); //output 15
console.log(x ^= y); //output 15
console.log(x <<= y); //output 15 << 10 = 15360
console.log(x >>= y); //output 15 >> 10 = 0
